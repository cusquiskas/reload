var marcasHome = class {


    constructor(modulo) {

        let HTMLOrigen = '<li> <a class="panel-trigger pincharmarcas {{nombre}}" data-target="{{nombre}}"> <img src="res/img/marcas/logo-{{nombreimagen}}" alt="" />   </a>  </li>';



        let HTMLONav = '<div class="{{nombrenav}}"> <h1>"{{titulo}}"</h1> <p>{{texto}} </p> <p><a href="{{link}}" target="_blank">{{link}}</a></p>{{botones}} </div>';


        let ul = document.getElementById('ul-marcas');
        for (let i=0; i <= arraymarcas.marcas.length; i++) {
            let HTML = $.parseHTML(HTMLOrigen.reemplazaMostachos(arraymarcas.marcas[i]));
            $('#ul-marcas').append(HTML);
        }

        let nav =document.getElementById("nav-marcas");
        for(let k=0; k<=marcasdiv.marcas.length;k++){
            let HTLMNAV = $.parseHTML(HTMLONav.reemplazaMostachos(marcasdiv.marcas[k]));
            $('#nav-marcas').append(HTLMNAV);

        }

        Moduls.getApp().getScript().conmutaTexto();

        $('.pincharmarcas').on("click", function () {
            let panelantiguo = $('.pincharmarcas');
            panelantiguo.removeClass('active');

            let activeClassName = 'active',
                trigger = $(this),
                panel = $('.' + trigger.attr('data-target')),
                isOpening = !(panel.hasClass(activeClassName));


            if (!panel.length) {
                console.warn('Cannot find panel with id ' + trigger.attr('data-target') + ' in trigger:', this);
                return;
            }

            panel.trigger('panel' + (isOpening ? 'opening' : 'closing'))

            trigger[isOpening ? 'addClass' : 'removeClass'](activeClassName);
            panel[isOpening ? 'slideDown' : 'slideUp'](function () {
                panel[isOpening ? 'addClass' : 'removeClass'](activeClassName)
                    .trigger('panel' + (isOpening ? 'opened' : 'closed'))
                    .get(0).style.display = '';
            });
        });

        // Close all inner pannels when closing sidebar menu section:
        $(document.body).on('panelclosed', '.sidebar-navigation .panel', function () {
            $('.active', this).removeClass('active');
        });
        $('.brand-side-info').on('panelopening', 'div', function () {
            $(this).siblings().hide().removeClass('active');
        });
        $('.mini-module-brand-list').on('click', 'a', function () {
            $(this).parent('li').siblings().find('a.active').removeClass('active');
        });

        $('.marcasappflota').click(function () {
            Moduls.getApp().getScript().marcaSeccion = 'Flota';
            Moduls.getMarcacontent().load({
                url: 'modulos/marcas/AirEuropa/menu.html',
                script: true,
                class: 'menuAirEuropa'
            });
        });

        $('.marcasappdestino').click(function () {
            //14293 - 29.04.2019 - al template App, marcaContent lo destrullo con el siguiente load
            Moduls.getApp().getScript().marcaSeccion = 'Destino';
            Moduls.getMarcacontent().load({
                url: 'modulos/marcas/AirEuropa/menu.html',
                script: true,
                class: 'menuAirEuropa'
            });
        });

    }


}



